/*

    onkeydown = dispara a função antes de aparecer o valor
    onkeypress = só vale para teclas q possuem um valor (CTRL + ALT + FN não possuem) // igual ao keydown
    onkeyup = dispara a função apos a apresentação do valor

*/

function alertOnKeyDown() {
    alert("Evento keydown disparado")
}

function alertOnKeyUp() {
    alert("Evento keyup disparado")
}
